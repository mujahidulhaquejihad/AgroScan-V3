let currentLang = localStorage.getItem("agrovet_lang") || "bn";

function t(key, vars) {
  let s = (I18N[currentLang] && I18N[currentLang][key]) || I18N.en[key] || key;
  if (vars) {
    Object.keys(vars).forEach((k) => {
      s = s.replace(new RegExp("\\{" + k + "\\}", "g"), vars[k]);
    });
  }
  return s;
}

function govKey(abbr) {
  return String(abbr || "").replace(/-/g, "");
}

function govField(link, field) {
  const k = "gov_" + govKey(link.abbr) + "_" + field;
  return t(k) !== k ? t(k) : (link[field] || "");
}

function emField(phone, field) {
  const k = "em_" + phone + "_" + field;
  return t(k) !== k ? t(k) : "";
}

function applyI18n() {
  document.documentElement.lang = currentLang === "bn" ? "bn" : "en";
  const titleKey = document.body && document.body.getAttribute("data-title-key");
  document.title = t(titleKey || "page_title");
  document.querySelectorAll("[data-i18n]").forEach((el) => {
    el.textContent = t(el.getAttribute("data-i18n"));
  });
  document.querySelectorAll("[data-i18n-html]").forEach((el) => {
    el.innerHTML = t(el.getAttribute("data-i18n-html"));
  });
  document.querySelectorAll("[data-i18n-ph]").forEach((el) => {
    el.placeholder = t(el.getAttribute("data-i18n-ph"));
  });
  document.querySelectorAll("[data-i18n-title]").forEach((el) => {
    el.title = t(el.getAttribute("data-i18n-title"));
  });
  document.querySelectorAll("[data-i18n-aria]").forEach((el) => {
    el.setAttribute("aria-label", t(el.getAttribute("data-i18n-aria")));
  });
  const btn = document.getElementById("langToggle");
  if (btn) btn.textContent = currentLang === "en" ? "বাংলা" : "English";
  document.dispatchEvent(new CustomEvent("langchange", { detail: { lang: currentLang } }));
}

function toggleLang() {
  currentLang = currentLang === "en" ? "bn" : "en";
  localStorage.setItem("agrovet_lang", currentLang);
  applyI18n();
}

window.AgrovetI18n = { t, applyI18n, toggleLang, govField, emField, govKey, get lang() { return currentLang; } };

if (document.readyState === "loading") {
  document.addEventListener("DOMContentLoaded", applyI18n);
} else {
  applyI18n();
}
